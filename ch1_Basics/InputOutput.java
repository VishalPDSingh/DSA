
package ch1_Basics;

public class InputOutput {
    public static void main(String[] args) {
        
        // print() → prints output on the SAME line
        System.out.print("DSA Classes");
        System.out.print(" Vishal Singh");

        // println() → prints output and moves cursor to NEXT line
        System.out.println("Vishal Singh");
        System.out.println("JIS College Of Engineering");

        // Single-line comment → ignored by compiler
        // Used for explanation/documentation

        // Escape sequence \n → moves text to NEXT LINE
        System.out.println("Vishal\nSingh");
    }
}

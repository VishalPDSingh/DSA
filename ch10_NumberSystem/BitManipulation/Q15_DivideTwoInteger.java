package ch10_NumberSystem.BitManipulation;

/*
 * Divide Two Integers
 * 
 * ---------------------------------------------------
 * Problem:
 * 
 * Divide two integers without using:
 * 
 * 1) *
 * 2) /
 * 3) %
 * ---------------------------------------------------
 * 
 * Example:
 * 
 * 48 / 5
 * 
 * Quotient = 9
 * ---------------------------------------------------
 * 
 * Idea 1:
 * 
 * Repeated subtraction
 * 
 * 48 - 5 = 43
 * 43 - 5 = 38
 * 38 - 5 = 33
 * ...
 * 
 * Count total subtraction operations.
 * ---------------------------------------------------
 * 
 * TC:
 * O(a / b)
 * ---------------------------------------------------
 * 
 * Idea 2:
 * 
 * Use Bit Manipulation
 * ---------------------------------------------------
 * 
 * Main Logic:
 * 
 * We know:
 * 
 * b << i
 * 
 * means:
 * 
 * b * (2^i)
 * 
 * ---------------------------------------------------
 * 
 * Example:
 * 
 * 5 << 3
 * 
 * =
 * 
 * 5 * 8
 * 
 * =
 * 
 * 40
 * ---------------------------------------------------
 * 
 * For:
 * 
 * 48 / 5
 * 
 * Try largest powers of 2.
 * ---------------------------------------------------
 * 
 * 5 * 8 = 40
 * 
 * Remaining:
 * 
 * 48 - 40 = 8
 * ---------------------------------------------------
 * 
 * Next:
 * 
 * 5 * 1 = 5
 * 
 * Remaining:
 * 
 * 8 - 5 = 3
 * ---------------------------------------------------
 * 
 * Quotient:
 * 
 * 8 + 1 = 9
 * 
 * Binary:
 * 
 * 01001
 * ---------------------------------------------------
 * 
 * Time Complexity:
 * O(log n)
 * 
 * Space Complexity:
 * O(1)
 */

public class Q15_DivideTwoInteger {

    // ---------------------------------------------------
    // Idea 1 : Brute Force
    // ---------------------------------------------------

    static int division1(int a, int b) {

        int count = 0;

        while (a >= b) {

            a = a - b;

            count++;
        }

        return count;
    }

    // ---------------------------------------------------
    // Idea 2 : Optimal
    // ---------------------------------------------------

    static int division2(int a, int b) {

        int quotient = 0;

        /*
         * Start from highest bit
         */

        for (int i = 31; i >= 0; i--) {

            /*
             * Check:
             * 
             * b * (2^i)
             * <= a
             */

            if ((b << i) <= a) {

                /*
                 * Subtract value
                 */

                a = a - (b << i);

                /*
                 * Add 2^i into quotient
                 */

                quotient = quotient | (1 << i);
            }
        }

        return quotient;
    }

    public static void main(String[] args) {

        int a = 48;

        int b = 5;

        int ans1 = division1(a, b);

        System.out.println("Brute Force Quotient: " + ans1);

        int ans2 = division2(a, b);

        System.out.println("Optimal Quotient: " + ans2);
    }
}
/*
---------------------------------------------------
Problem
---------------------------------------------------

Divide two integers without using:

1) Multiplication (*)
2) Division (/)
3) Mod Operator (%)

---------------------------------------------------
Examples
---------------------------------------------------

30 / 7  = 4
48 / 5  = 9
5  / 2  = 2

---------------------------------------------------
Main Idea
---------------------------------------------------

Division means:

How many times divisor can be subtracted
from dividend.

---------------------------------------------------
Example 1
---------------------------------------------------

30 / 7

30 - 7 = 23   count = 1
23 - 7 = 16   count = 2
16 - 7 = 9    count = 3
9  - 7 = 2    count = 4

Now 2 < 7

Therefore stop.

Answer = 4

---------------------------------------------------
Example 2
---------------------------------------------------

48 / 5

48 - 5 = 43   count = 1
43 - 5 = 38   count = 2
38 - 5 = 33   count = 3
33 - 5 = 28   count = 4
28 - 5 = 23   count = 5
23 - 5 = 18   count = 6
18 - 5 = 13   count = 7
13 - 5 = 8    count = 8
8  - 5 = 3    count = 9

Now 3 < 5

Therefore stop.

Answer = 9

---------------------------------------------------
Example 3
---------------------------------------------------

5 / 2

5 - 2 = 3   count = 1
3 - 2 = 1   count = 2

Now 1 < 2

Therefore stop.

Answer = 2

---------------------------------------------------
Observation
---------------------------------------------------

Division can be converted into
Repeated Subtraction.

---------------------------------------------------
Pseudo Logic
---------------------------------------------------

count = 0

while(dividend >= divisor)
{
    dividend = dividend - divisor;
    count++;
}

answer = count

---------------------------------------------------
Time Complexity
---------------------------------------------------

O(dividend / divisor)

---------------------------------------------------
*/

/*
---------------------------------------------------
Optimal Approach
---------------------------------------------------

Instead of subtracting divisor one by one,

use BIT MANIPULATION.

---------------------------------------------------
Main Idea
---------------------------------------------------

Find biggest power of 2 such that:

(divisor << i) <= dividend

Then subtract it at once.

---------------------------------------------------
Why?
---------------------------------------------------

(divisor << i)

means:

divisor * (2^i)

So instead of subtracting divisor
1 time,

we subtract many times together.

---------------------------------------------------
Example
---------------------------------------------------

30 / 7

---------------------------------------------------
Step 1
---------------------------------------------------

Find largest shift:

7 << 2 = 28

because:

7 * 4 = 28

and:

28 <= 30

---------------------------------------------------
Step 2
---------------------------------------------------

Subtract:

30 - 28 = 2

Add contribution:

answer = answer + 4

because:

2^2 = 4

---------------------------------------------------
Step 3
---------------------------------------------------

Now dividend = 2

2 < 7

Stop.

---------------------------------------------------
Final Answer
---------------------------------------------------

4

---------------------------------------------------
Another Example
---------------------------------------------------

48 / 5

---------------------------------------------------
Find largest shift
---------------------------------------------------

5 << 3 = 40

because:

5 * 8 = 40

---------------------------------------------------
Subtract
---------------------------------------------------

48 - 40 = 8

answer = 8

---------------------------------------------------
Again
---------------------------------------------------

5 << 0 = 5

8 - 5 = 3

answer = 8 + 1 = 9

---------------------------------------------------
Final Answer
---------------------------------------------------

9

---------------------------------------------------
Pseudo Logic
---------------------------------------------------

answer = 0

for(i = 31 -> 0)
{
    if((divisor << i) <= dividend)
    {
        dividend = dividend - (divisor << i);

        answer = answer + (1 << i);
    }
}

---------------------------------------------------
Time Complexity
---------------------------------------------------

O(32)
≈ O(1)

---------------------------------------------------
Important Observation
---------------------------------------------------

Left Shift:

x << i

means:

x * (2^i)

---------------------------------------------------
*/